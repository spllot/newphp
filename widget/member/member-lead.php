<p class="text"><strong>買賣撮合機制說明</strong></p>
<p class="text0"><span>在此進一步說明買賣撮合與商品即時遞送機制之運作，因為本平台既可得到買家與商家的位置，故在買家搜尋商品之時，前台之商品陳列便乃以買家為中心，依據與買家之距離遠近陳列商家商品，若買家需求商家的商品進行購買，且買家之位置同時能符合商家於APP設定之遞送商品距離，則將達成系統買賣撮合成交之條件，進而商家會依循買家收件地址， 以系統路徑規劃做指引即時遞送商品給買家，這就是本系統的買賣撮合機制，此外，買賣即時撮合需補述的是，此機制僅支援金流商品服務 (只限刷卡)，若您設置為非金流商品，則需要買賣雙方以電話聯繫之人工撮合來達成 ; </span>若商家行動商品或服務也支援即時遞送到府者，不論列表為金流服務或非金流服務，皆可在前台服務列表圖片上張貼[即時遞送]標籤 (參考圖五所示)，此標籤可以讓消費者明膫商家支援此服務，商家設置此標籤只須經由行動APP便可隨時設置，詳細設置方法可以參考<a href="training_11.html" target="_blank">行動APP之功能介紹</a>。</p>
