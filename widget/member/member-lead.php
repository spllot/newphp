<p class="text0"><span class="text01"><strong>點選 &quot;D&rdquo;項:</strong></span>此設置可以變更行動服務之定點位置 (例如, 行動餐車可變更販售地點在不同之風景區位置)，而變更之服務地點必須輸入在電腦頁面之[手動設定行動服務位置]項目(如圖四所示)，<span>才能對行動服務之位置更新，若位置更新，則前台服務列表圖片會貼上[行動商家]標籤 (參考圖五所示)。若對固定位置商店而言，則無需點選設置[手動設定行動服務位置]變更商店位置。</span></p>
<p class="text0"><span class="text01"><strong>點選 &quot;E&rdquo;項: </strong></span>此設置用於位置來源來自手機定位系統 (若為隨時隨地移動之服務，建議點選此項目偵測移動位置)，商家經由此設定可以行動服務在不同之方位， 當系統偵測商家位置移動時，前台服務列表圖片也會貼上[行動商家]標籤)， 須注意的是商家必需使用手機行動APP登入本站，才能有此服務功能之支援。</p>
