<p class="text"><span class="text"><strong>本地服務營業狀態設定</strong></span></p>
<p class="text0">如圖二<span class="text0"><strong>，「本地服務營業狀態設定」</strong></span>我們將點選項目設為 &quot;A&quot;<span class="text4">、&quot;</span>B&quot;<span class="text4">、&quot;</span>C&quot;三項，逐項來說明其設定之功能用途，以讓會員能根據自家服務型態，做出最適合之設定。</p>
<p class="text0"><span class="text01"><strong>點選 &quot;A&rdquo;項: </strong></span>此模式為商品或服務皆保持在前台上架販售狀態，所以適用於24hr營運之商品或服務。</p>
<p class="text0"><span class="text01"><strong>點選 &quot;B&rdquo;項:</strong></span> 此模式支援商品或服務暫時停止販售之設定，此模式設定後，商品或服務在前台即呈現為下架狀態，當商品或服務要再度上架，則取消此模式點選設定即可。</p>
<p class="text0"><span class="text01"><strong>點選 &quot;C&rdquo;項: </strong></span>此模式適用於行動販售或行動服務模式 (包含[商品]行動販售，以及[運輸]、[人力]、[活動]服務模式)。當以行動車行動販售商品為例，此項目被點選設定後，當商家登入網站時，表示商家商品販售中, 則商品列於前台為上架營業狀態,   若商家登出網站時, 表示商家商品已停止販售, 則商品於前台為下架狀態。</p>
