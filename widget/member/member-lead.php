<p class="text"><strong>行動販售車設定範例說明</strong></p>
<p class="text0"><span>我們以行動車販售商品之範例做設定說明，因為行動車販售商品比一般固定店面的彈性較多，所以它可以延伸以下兩種商品販售模式:<span class="text3"><br />
    </span><span class="text01"><strong>一、行動車定點販售模式: (點選&quot;C&rdquo;項 + 點選&quot;D&rdquo;項)</strong></span><br />
  行動車販售商品以定點位置販售，買家仍需自行到販售地點消費商品，這對買家消費而言，如同到一般固定店面之消費商品無異</span>，所不同的是此模式之定點販售位置可以做變更<span>，也就是說商家商店可以設定在不同的販售地點，這種方式即是一般所見行動車設置在定點風景區販售一般，如果要達成這種販售模式,   則需完成點選&quot;C&rdquo;項 + 點選&quot;D&rdquo;項即可。<br />
  <strong><br />
    </strong><span class="text01"><strong>二、行動車即時遞送商品模式: (點選&quot;C&rdquo;項+ 點選&quot;E&rdquo;項)</strong></span><br />
  行動車販售商品若以移動模式運作，則圖四須點選在&quot;E&rdquo;項方能達成功能，如前面所述因為位置之來源來自手機3G或GPS定位系統，所以商家位置可以移動且被系統偵測，因而可達到移動販售與即時遞送商品之目的，<span>在此提醒商家在車內使用手機定位時，車上若有貼有含金屬成分之隔熱紙，將會造成數十米的定位誤差 ; </span>另外，商家行動商品支援即時遞送到府者</span>，建議商家可以經由行動APP設置&quot;即時遞送&quot;標籤，設置此標籤能讓買家知道商家支援此服務而做呼求動作。</p>
