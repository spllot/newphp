    <h2 class="text"><span><span><strong>服務即得模式之應用架構</strong></span></span></h2>
<p class="text0">InTimeGo是「服務即得模式」的行動服務商務平台，係針對一般傳統商店、行動商店 (包含攤販、行動販售車、補貨物流車...等)、運輸工具以及本地人力與活動，進行即時動態服務搜尋的網路平台，對普羅大眾而言，這是一個能拉近供需距離與創造服務價值的網站，可以為買賣雙方創造更為便利與快速的商業關係。</p>
<p class="text0">所謂「即時服務」就是即購即得(服務即得)模式，消費者在「本地服務」搜尋得到的[商品]/[運輸]/[人力]/[活動]等，皆可獲得即購即得(服務即得)的服務機會。我們以[商品]服務為例，利用本站買賣雙定位搜尋的系統架構(如圖一. a)，不論以PC或行動裝置做服務搜尋，皆能以消費者為中心得到各商家服務的遠近方位排列，我們再根據商家不同的服務區別，可以得到消費者最短的到店取貨路徑，或是商家最短的遞貨到府路徑 ; 同樣若以[運輸]/[人力]/[活動]為例(如圖一. b)，也可以得到各服務相應於消費者的遠近關係，消費者可以依據商家之遠近與提供之條件，適當地選擇並購買其服務。</p>
