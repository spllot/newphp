<p class="text0"><span class="text01"><strong>點選 &quot;空車&rdquo;項:</strong></span> 當運輸業者在&quot;空車&quot;待客狀態，業者可以點選設定此項目，讓搭乘者可以看到運輸商品在前台顯示&quot;空車&quot;狀態。</p>
<p class="text0"><span class="text01"><strong>點選 &quot;載客中 (不開放共乘 或 共乘人數已額滿)&rdquo;項:</strong></span> 當運輸業者在&quot;載客&quot;狀態，業者需點選設定此項目，點選此項目也意涵不開放共乘 或 共乘人數已額滿。</p>
<p class="text0"><span class="text01"><strong>點選 &quot;載客中 (共乘)，行駛目標 : XXX&rdquo;項: </strong></span>當運輸業者在載客且支援共乘狀態下 (業者需徵詢乘客是否開放共乘)，業者可以點選設定此項目，同時運輸業者需輸入&quot;行駛目標&quot; ，此處因為輸入框限制填寫八個文字，所以目標地址僅需填寫到道路名稱即可，填寫完畢於前台列表將會顯示共乘行駛目標資訊，其他乘客看到此共乘資訊與路徑規劃，可以進而判斷是否要做電話呼求加入共乘 (可以跳至圖五參考[運輸]共乘範例，列表將會顯示&quot;共乘目標&quot;資訊，此時點選列表圖片進入服務說明頁，於[商家遞送路徑]會顯示共乘規劃路徑)。</p>
