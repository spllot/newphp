<td id="p1" style="text-align: left; padding-left: 10px; padding-top: 10px; display: none;" align="left">
<p style="text-align: justify;"><span style="font-size: medium; color: #666699; font-family: arial, helvetica, sans-serif;"><strong></strong><span style="color: #808080; font-size: small;">凡會員對站內之傳播式團購商品有介紹、購買或傳播行為，即成為本站傳播會員 ; 傳播會員可以獲得該商品所得之利潤做為介紹或傳播該商品之回饋，傳播會員可以同時介紹多個商品進入本站販售，賺取多個商品的介紹紅利，也可以在本站同時傳播多個商品資訊給友人，賺取多個商品的傳播紅利，本站商品傳播方式公開透明，可以提供商品販售資訊，讓會員們可以自行驗證自己的獲利。</span></span></p>
<p style="text-align: justify;"><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;"><strong><strong>第一類紅利回饋 (網站行銷人):</strong>&nbsp;</strong><span style="text-align: left;">提撥10%</span><span style="text-align: left;">「傳播紅利」做為</span><span style="text-align: left;">「部落格行銷推廣文章徵求辦法」之行銷活動獎勵，經由管理者評定會員獲取之分數，即可依據分數獲取「儲值金點數 (1點=1元)&nbsp;」回饋會員。</span><span style="text-align: left;">詳情請參考 [全民賺好康活動] &gt;&gt;「部落格推廣文章徵求辦法」。</span></span></p>
<p style="text-align: justify;"><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;"><strong>第二類紅利回饋</strong><strong> (</strong><strong>商品介紹人</strong><strong>): </strong>介紹傳播式團購商品進入本站販售之會員，可以獨得該商品之10%「傳播紅利」回饋，依據商品消費期結束，結算所得之紅利10%，轉為「儲值金點數 (1點=1元) 」直接回饋介紹商品之會員，商品之介紹人不可填寫商家本身。</span></p>
<p style="text-align: justify;"><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;"><strong>第三類紅利回饋 (商品傳播人):</strong>&nbsp;<span style="text-align: left;">對商品有購買(自購或介紹購買)與傳播行為(Email或臉書)之會員，可以共同得到該商品的80%傳播紅利回饋，會員依據獲取該商品的「傳播點數」，與總和「傳播點數」之比例，即可算得個人獲得儲值金之紅利。</span><span style="text-align: left;">所得之紅利將直接轉為「儲值金點數 (1點=1元)&nbsp;」回饋會員。 會員獲取「</span><span style="text-align: left;">傳播點數」之方式, 可以參考表(一)。</span></span></p>
<p style="text-align: justify;"><span style="color: #808080; font-family: arial, helvetica, sans-serif; font-size: small;"><strong>[說明].</strong>「傳播紅利」之定義: 傳播式團購商品加收10%之「服務費用」， 即是做為回饋傳播會員之傳播福利金，此福利金需另扣除其值之10%費用，做為稅金與公益支出款項 (包含5%營業稅金、5%公益基金)，其餘的90% 即做為商品之「傳播紅利」。</span></p>
