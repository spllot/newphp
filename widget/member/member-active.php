<p><span style="color: #808080; font-size: small;"><span style="font-family: arial, helvetica, sans-serif;"><strong>「傳播點數」轉換為「儲值金點數」的計</strong></span><strong style="font-size: 10px; color: #666699; font-family: arial, helvetica, sans-serif;">算方法:</strong></span></p>
<p><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;">團購商品兩週的販售期間內，買家小毛對商品A及商品B做商品傳播 (購買&amp;傳播行為)，獲得傳播點數如下:&nbsp;</span></p>
<p><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;">商品A獲得傳播點數100點 (商品A之傳播總點數累計為800點)</span></p>
<p><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;">商品B獲得傳播點數250點 (商品B之傳播總點數累計為2000點)</span></p>
<p><span style="font-size: small; color: #808080; font-family: arial, helvetica, sans-serif;">商品之消費時間結束, 結算商品分享之紅利金如下:</span></p>
<p><span style="color: #808080; font-size: small;"><span style="font-family: arial, helvetica, sans-serif;">商品A之傳播紅利為$52000元，</span><span style="font-family: arial, helvetica, sans-serif;">商品B之傳</span><span style="font-family: arial, helvetica, sans-serif;">播</span><span style="font-family: arial, helvetica, sans-serif;">紅利為$37000元</span></span></p>
<p><span style="color: #808080; font-size: small; font-family: arial, helvetica, sans-serif;">所以，小毛於商品A獲得的儲值金點數 = $52000 X (100/800)= 6500 點</span></p>
<p><span style="color: #808080; font-size: small; font-family: arial, helvetica, sans-serif;">小毛於商品B獲得的儲值金點數 = $37000 X (250/2000)= 4625 點</span></p>
<p><span style="color: #808080; font-size: small; font-family: arial, helvetica, sans-serif;">小毛以兩週的時間對團購商品A與B進行傳播，而獲得總值11125點的儲值金點數，因為儲值金點數 1點=1元，所以小毛等於獲取11125元的紅利金入袋。</span></p>
